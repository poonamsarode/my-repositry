package session11;

public class _4_MainOverload {
	
public static void main(String[] args) {
	
	System.out.println("hi");
	                                     
}	
	
public static void main ( int p)
{  
	
	System.out.println(" value of p is " + p);        //only hi got printed 
	
}
                                                      // can be overload a main method ?  yesss  
public static void main ( int p, int q) 
{
    System.out.println(" value of p & q is " + q);	
}	
	
	






}





